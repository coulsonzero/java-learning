package BasicConcepts;

public class output {
    public static void main(String[] args) {
        // 换行
        System.out.println("hello world");
        // 不换行
        System.out.print("please enter a number: \n");
        // 格式化输出
        System.out.printf("name: %s, age: %d \n", "tom", 12);
    }
}
